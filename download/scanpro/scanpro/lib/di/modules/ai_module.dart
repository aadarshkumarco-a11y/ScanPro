/// Structured summary of a document generated by Gemini.
class DocumentSummary {
  const DocumentSummary({
    required this.id,
    required this.documentId,
    required this.summary,
    required this.keyPoints,
    required this.tldr,
    required this.generatedAt,
  });

  /// Unique identifier for this summary.
  final String id;

  /// ID of the source document.
  final String documentId;

  /// Multi-paragraph summary of the document content.
  final String summary;

  /// Bullet-point list of key takeaways.
  final List<String> keyPoints;

  /// One-line "too long; didn't read" summary.
  final String tldr;

  /// Timestamp when the summary was generated.
  final DateTime generatedAt;
}

/// Structured data extracted from a document by Gemini.
class ExtractedData {
  const ExtractedData({
    required this.id,
    required this.documentId,
    required this.fields,
    required this.tables,
    required this.extractedAt,
  });

  /// Unique identifier for this extraction.
  final String id;

  /// ID of the source document.
  final String documentId;

  /// Key-value pairs of extracted fields (e.g., 'total': '\$42.50').
  final Map<String, String> fields;

  /// Extracted table data as rows of string maps.
  final List<Map<String, String>> tables;

  /// Timestamp when the extraction was performed.
  final DateTime extractedAt;
}

/// Classification result for a document.
class DocumentClassification {
  const DocumentClassification({
    required this.category,
    required this.confidence,
    required this.suggestedFolder,
    required this.tags,
  });

  /// Primary document category (e.g., 'invoice', 'receipt', 'contract').
  final String category;

  /// Confidence score for the classification (0.0–1.0).
  final double confidence;

  /// Suggested folder name based on the classification.
  final String suggestedFolder;

  /// Additional tags for fine-grained categorization.
  final List<String> tags;
}
