/// AI feature module — provides all Riverpod providers related to
/// Google Gemini AI integration for document summarization, data
/// extraction, and intelligent content analysis.
///
/// The module exposes the Gemini service, AI repository for persisting
/// AI-generated results, and use cases for summarization and extraction.
library;

import 'package:flutter_riverpod/flutter_riverpod.dart';

import '../../domain/repositories/ai_repository.dart';
import '../../domain/usecases/ai/summarize_document_usecase.dart';
import '../../domain/usecases/ai/extract_data_usecase.dart';
import '../../domain/usecases/ai/classify_document_usecase.dart';
import '../../data/datasources/ai_local_data_source.dart';
import '../../data/repositories/ai_repository_impl.dart';
import '../injection.dart';

// ---------------------------------------------------------------------------
// Data Sources
// ---------------------------------------------------------------------------

/// Local data source that caches AI-generated summaries, extracted data,
/// and document classifications in Hive for offline access.
final aiLocalDataSourceProvider = Provider<AILocalDataSource>((ref) {
  final box = ref.watch(hiveBoxProvider);
  return AILocalDataSource(box: box);
});

// ---------------------------------------------------------------------------
// Services
// ---------------------------------------------------------------------------

/// Gemini AI service wrapping the Google Generative AI SDK.
///
/// Supports multimodal input (text + images) for document analysis.
/// Requires a valid API key configured in the app's secure storage.
final geminiServiceProvider = Provider<GeminiService>((ref) {
  final isOnline = ref.watch(isOnlineProvider);
  return GeminiService(isOnline: isOnline);
});

// ---------------------------------------------------------------------------
// Repository
// ---------------------------------------------------------------------------

/// Primary [AIRepository] implementation backed by the Gemini service
/// and local Hive cache.
final aiRepositoryProvider = Provider<AIRepository>((ref) {
  final localDataSource = ref.watch(aiLocalDataSourceProvider);
  final geminiService = ref.watch(geminiServiceProvider);
  return AIRepositoryImpl(
    localDataSource: localDataSource,
    geminiService: geminiService,
  );
});

// ---------------------------------------------------------------------------
// Use Cases
// ---------------------------------------------------------------------------

/// Generates a concise summary of the document at [documentId] using
/// Gemini. Returns a [DocumentSummary] with key points and a one-line
/// TL;DR.
final summarizeDocumentUseCaseProvider =
    Provider<SummarizeDocumentUseCase>((ref) {
  final repository = ref.watch(aiRepositoryProvider);
  return SummarizeDocumentUseCase(repository: repository);
});

/// Extracts structured data (tables, key-value pairs, dates, amounts)
/// from a document using Gemini's multimodal capabilities.
final extractDataUseCaseProvider = Provider<ExtractDataUseCase>((ref) {
  final repository = ref.watch(aiRepositoryProvider);
  return ExtractDataUseCase(repository: repository);
});

/// Classifies a document into a category (invoice, receipt, contract,
/// ID, notes, etc.) using Gemini for intelligent folder suggestions.
final classifyDocumentUseCaseProvider =
    Provider<ClassifyDocumentUseCase>((ref) {
  final repository = ref.watch(aiRepositoryProvider);
  return ClassifyDocumentUseCase(repository: repository);
});

